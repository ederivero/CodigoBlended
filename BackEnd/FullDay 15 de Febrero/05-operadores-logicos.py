# Operadores Logicos
# and => si todas las condiciones son verdaderas todo es verdadero
# or => si una como minimo de las condiciones es verdadera todo es verdadero
# not => si es falso es verdadero y viceversa
# en JAVASCRIPT SE REPRESENTA DE LA SIGUIENTE MANERA
# and => &&
# or => ||
# not => not
print((10>10) and (20>10))
print((10>10) or (20>10))
print(not(10<20))